package com.pojo;

public class User {
    private String name;
    private String pwd;
    private int sex;
    private String info;

    public String getName() {
        return name;
    }

    public String getPwd() {
        return pwd;
    }

    public int getSex() {
        return sex;
    }

    public String getInfo() {
        return info;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }

    public void setSex(int sex) {
        this.sex = sex;
    }

    public void setInfo(String info) {
        this.info = info;
    }
}
